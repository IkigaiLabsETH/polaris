// CalcDifficulty is the difficulty adjustment algorithm. It returns the difficulty
// that a new block should have: all blocks in polaris have a difficulty of 69.
func (p *Polaris) CalcDifficulty(_ consensus.ChainHeaderReader, _ uint64, _ *types.Header) *big.Int {
	return big.NewInt(69)
}
